  // Function to render project cards
  function renderProjects() {
    const container = document.querySelector('.projects-container');
    if (!container) return;
  
    projects.forEach(project => {
      const card = document.createElement('div');
      card.className = 'project-card';
      card.dataset.projectId = project.id;
      
      card.innerHTML = `
        <div class="project-image">
          <img src="${project.image}" alt="${project.title}">
        </div>
        <div class="project-content">
          <h3 class="project-title">${project.title}</h3>
          <p class="project-description">${project.shortDescription}</p>
          <div class="project-tags">
            ${project.tags.map(tag => `<span class="project-tag">${tag}</span>`).join('')}
          </div>
        </div>
      `;
      
      container.appendChild(card);
      
      // Add click event to open modal
      card.addEventListener('click', () => openProjectModal(project.id));
    });
  }
  
  // Function to open project modal
  function openProjectModal(projectId) {
    const modal = document.querySelector('.project-details-modal');
    const project = projects.find(p => p.id === projectId);
    
    if (!modal || !project) return;
    
    // Update modal content
    modal.querySelector('.project-title').textContent = project.title;
    modal.querySelector('.project-description').textContent = project.description;
    
    // Update links
    const appStoreLink = modal.querySelector('.app-store-link');
    const caseStudyLink = modal.querySelector('.case-study-link');
    
    appStoreLink.href = project.appStoreLink;
    caseStudyLink.href = project.caseStudyLink;
    
    // Update tech stack
    const techStack = modal.querySelector('.project-tech-stack');
    techStack.innerHTML = `
      <h4 class="tech-stack-title">Technologies Used</h4>
      <div class="tech-stack-items">
        ${project.techStack.map(tech => `
          <div class="tech-item">
            ${tech.icon ? `<span class="tech-item-icon">${tech.icon}</span>` : ''}
            ${tech.name}
          </div>
        `).join('')}
      </div>
    `;
    
    // Show the modal
    modal.style.opacity = '1';
    modal.style.visibility = 'visible';
    
    // If we have project preview functionality, update it
    if (window.projectPreviewHandler && typeof window.projectPreviewHandler.updateProjectPreview === 'function') {
      window.projectPreviewHandler.updateProjectPreview(project);
    }
  }
  
  // Function to close project modal
  function closeProjectModal() {
    const modal = document.querySelector('.project-details-modal');
    if (!modal) return;
    
    modal.style.opacity = '0';
    modal.style.visibility = 'hidden';
  }
  
  // Set up project functionality
  function setupProjects() {
    renderProjects();
    
    // Add close button event listener
    const closeButton = document.querySelector('.modal-close');
    if (closeButton) {
      closeButton.addEventListener('click', closeProjectModal);
    }
    
    // Close modal when clicking outside
    const modal = document.querySelector('.project-details-modal');
    if (modal) {
      modal.addEventListener('click', (e) => {
        if (e.target === modal) {
          closeProjectModal();
        }
      });
    }
  }
  
  export { setupProjects, projects };